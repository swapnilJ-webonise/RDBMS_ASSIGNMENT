CREATE TABLE variant( 
id INT PRIMARY KEY, 
product_id INT, 
color VARCHAR(20), 
price DOUBLE (7,3), 
size VARCHAR(20), 
cart_id INT, 
FOREIGN KEY (product_id) REFERENCES product(id),
FOREIGN KEY (cart_id) REFERENCES cart_details(id)
 );


